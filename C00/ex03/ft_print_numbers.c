#include <unistd.h>

void	ft_print_numbers(void)
{
	int		nbr;
	char	nb;

	nbr = 0;
	while (nbr < 10)
	{
		nb = nbr + '0';
		write(1, &nb, 1);
		nbr++ ;
	}
}
